/* CRIA SHORTCODE PARA MENU DINAMICO */
function shortcode_menu_dinamico($atts, $content = null) {
	extract(
            shortcode_atts(
                array(  
                    'menu'            => '', 
                    'container'       => 'div', 
                    'container_class' => '', 
                    'container_id'    => '', 
                    'menu_class'      => 'menu', 
                    'menu_id'         => '',
                    'echo'            => true,
                    'fallback_cb'     => 'wp_page_menu',
                    'before'          => '',
                    'after'           => '',
                    'link_before'     => '',
                    'link_after'      => '',
                    'depth'           => 0,
                    'walker'          => '',
                    'theme_location'  => ''
                ), 
            $atts)
	);
 
 
	$config = array( 
		'menu'            => $menu, 
		'container'       => $container, 
		'container_class' => $container_class, 
		'container_id'    => $container_id, 
		'menu_class'      => $menu_class, 
		'menu_id'         => $menu_id,
		'echo'            => false,
		'fallback_cb'     => $fallback_cb,
		'before'          => $before,
		'after'           => $after,
		'link_before'     => $link_before,
		'link_after'      => $link_after,
		'depth'           => $depth,
		'walker'          => $walker,
		'theme_location'  => $theme_location
	);
	
	return wp_nav_menu($config);
}
add_shortcode("wpmenu", "shortcode_menu_dinamico");

